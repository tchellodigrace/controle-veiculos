CREATE TABLE IF NOT EXISTS usuarios (
  id SERIAL PRIMARY KEY,
  nome VARCHAR(100) NOT NULL,
  usuario VARCHAR(50) UNIQUE NOT NULL,
  senha VARCHAR(255) NOT NULL,
  ativo BOOLEAN DEFAULT TRUE,
  criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS registros (
  id SERIAL PRIMARY KEY,
  usuario_id INTEGER REFERENCES usuarios(id),
  chegada VARCHAR(10) NOT NULL,
  placa VARCHAR(20) NOT NULL,
  modelo VARCHAR(100) DEFAULT '',
  finalidade VARCHAR(100) DEFAULT '',
  empresa VARCHAR(200) NOT NULL,
  motorista VARCHAR(200) DEFAULT '',
  cnh VARCHAR(50) DEFAULT '',
  entrada VARCHAR(10) NOT NULL,
  saida VARCHAR(10) DEFAULT '',
  nota VARCHAR(100) DEFAULT '',
  obs TEXT DEFAULT '',
  data_registro DATE DEFAULT CURRENT_DATE,
  criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS motoristas (
  id SERIAL PRIMARY KEY,
  motorista VARCHAR(200) NOT NULL,
  cnh VARCHAR(50) DEFAULT '',
  placa VARCHAR(20) DEFAULT '',
  empresa VARCHAR(200) DEFAULT ''
);

